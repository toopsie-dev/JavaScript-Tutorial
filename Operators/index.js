// Arithmetic Operators
let x = 5;
let y = 12;

console.log(x + y);
console.log(y % x);

// Comparison Operators
let isGreater = y != x;
console.log(isGreater);

// Logical Operators
console.log(x > 5 && y < 10);
console.log(x > 5 || y < 15);
console.log(x === y);

// Ternary Operators
let grade = 81;
let finalGrade = grade > 90 ? "A" : grade > 80 ? "B" : grade > 70 ? "C" : "F";
console.log(finalGrade);
